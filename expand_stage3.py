def expand_stage3():
    """扩充第三阶段场景"""
    
    # 读取文件
    with open('/root/Sync/Code/game-scifi-hybrid/twine_version_v16_final.html', 'r', encoding='utf-8') as f:
        content = f.read()
    
    original_chars = len(content)
    
    # 替换场景
    for scene_id, scene_data in STAGE3_EXPANSIONS.items():
        old_pattern = f'"{scene_id}": {{'
        start_idx = content.find(old_pattern)
        
        if start_idx == -1:
            print(f"警告：未找到场景 {scene_id}")
            continue
        
        # 找到场景结束
        next_idx = content.find('},', start_idx + len(old_pattern))
        if next_idx == -1:
            next_idx = content.find('};', start_idx + len(old_pattern))
        
        old_scene = content[start_idx:next_idx + 2]
        
        # 构建新场景
        choices_str = ',\n                    '.join([
            f'{{ text: "{c["text"]}", next: "{c["next"]}"' + (f', concept: "{c["concept"]}"' if "concept" in c else "") + ' }'
            for c in scene_data['choices']
        ])
        
        new_scene = f'''"{scene_id}": {{
                title: "{scene_data['title']}",
                text: `{scene_data['text']}`,
                choices: [
                    {choices_str}
                ]
            }}'''
        
        content = content.replace(old_scene, new_scene)
        print(f"已扩充场景：{scene_id}")
    
    # 写入文件
    with open('/root/Sync/Code/game-scifi-hybrid/twine_version_v16_final.html', 'w', encoding='utf-8') as f:
        f.write(content)
    
    new_chars = len(content)
    
    print(f"\n第三阶段扩充完成！")
    print(f"原文件大小：{original_chars} 字符")
    print(f"新文件大小：{new_chars} 字符")
    print(f"增加：{new_chars - original_chars} 字符 ({(new_chars/original_chars - 1) * 100:.1f}%)")

if __name__ == '__main__':
    expand_stage3()
